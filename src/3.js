/* Poor Typing
  The fact that a language is poorly typed in the case of javascript
  does not mean that it does not have a type,
  but rather that the variables can change type, if we wish.
*/
'use strict';

let chocolate = 'Delicious'

console.log(chocolate)

// printing the value of this type - to know if it is string, number ...
console.log(typeof chocolate)

// assigning float value to same variable
chocolate = 17.5
console.log(chocolate)
console.log(typeof chocolate)

// The variable has a numeric type

