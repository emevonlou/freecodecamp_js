// Assignment Operators

const a = 9;
let b = 4;

// Assignments using shortcuts
// Additive assignment:
b += a; // b = b + a;
console.log(b);

// Subtraction assignment:
b -= 4; // b = b - 4;
console.log(b);

// Multiplicative assignment:
b *= 2; // b = b * 2;
console.log(b);

// Divisive assignment:
b /= 2; // b = b / 2;
console.log(b);

// Modular assignment:
b %= 2; // b = b % 2;
console.log(b);